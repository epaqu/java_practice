
public class AverageCalculator {

	public static void main (String[] args)
	{
		Integer x = 1; //declaring x as type integer = 1
		Integer y = 7; //declaring y as type integer = 7
		Integer z = 9; //declaring z as type integer = 9
		Integer w = 34; //declaring w as type integer = 34
		Double average; //declaring average as type integer
		average = (x + y + z + w) / 4.0;
		System.out.println("The average of 1, 7, 9, and 34 is " + average);
		
	}
}